# Version control
# Git terminology:
  # Repository - Repo
  # Commit - save changes - snapshot 
  # Push: update repository with edits
  # Pull: updating local version of the repository to the current version
  # Stage: preparan antes de commit
  # Branch: ramificaciones del archivo original que trabajo y aún no he hecho push
  # Merge: unión del branch con el archivo originsl
  # Conflict: muchos cambios por los usuarios y Git no es capaz de saber cómo unirlos